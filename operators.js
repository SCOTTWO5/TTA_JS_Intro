// Arithmetic operators
let a = 10;
let b = 5;
let result = a + b;
console.log(result); // 15

// Compound Assignment Operator
let x = 10;
x +=5;
console.log; // 15

// Comparison Operator
let isEqual = (a == b);
console.log(isEqual); // false

// Logical Operator
let logicalResult = (a > 5 && b < 10);
console.log(logicalResult); // true

// String Operator
let fullName = "John" + " " + "Doe";
console.log(fullName); // John Doe

// Terinary Operator
let max = (a < b) ? a : b;
console.log(max); // 10